								<h3>WHAT IS OSTEOPATHY?</h3>
							</div>
	
	
	
	
	
	
	
	<p style="font-size:18px;">Osteopathic Manual Therapy is a hands on therapy with the simple aim of removing unwanted mechanical and neurophysiological stress and strain on the body in order to restore efficient coordination and a vital internal environment. Utilizing safe and gentle techniques, it supports the interdependent functionality of the structural (bones and joints), myofascial (muscles and fascia), cranial (membranes and fluids) and visceral (organs and related tissues) systems. 
<br><br>
With an extensive knowledge of anatomy, physiology and biomechanics, osteopathic manual therapists use a refined sense of touch to identify and treat asymmetric motion and altered tissue states in the body. Assessment and treatment happen simultaneously and are fine tuned to individual patients’ needs
<br><br>
The far reaching effects of osteopathic care can include reduction or elimination of pain, greater sense of ease in motion, improved energy levels, deeper and fuller breath and improved sleep. 

</p>
<br>
	<div class="heading-block border-bottom-0">
								<h3>OSTEOPATHIC PRINCIPLES</h3>
							</div>
	


<p style="font-size:18px;">
The four main principles of osteopathy convey the underlying philosophy of the osteopathic approach towards health and wellness.
<ul style="font-size:18px;">
<li> The body is a functional unit.</li><br>
<li>The body is capable of self healing, auto regulation and health maintenance. Treatment facilitates these natural processes by removing barriers.</li><br>
<li>Structure and function are reciprocally interrelated; thus any departure from normal position or mobility can result in dysfunction locally or elsewhere in the body.</li><br>
<li>Rational treatment is based upon an understanding of the basic principles of body unity, self-regulation, and the interrelationship of structure and function.</li>
</ul>

<span style="font-size:18px;">It is the application of these principles that defines osteopathic care in relation to other alternative health care methods.</span>
</p>

